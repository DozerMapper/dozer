package org.dozer.vo;

public class TestReferenceFoo extends BaseTestObject {
  private String a;
  private String b;
  private String c;

  @Override
  public boolean equals(Object obj) {
    if (this == obj) {
      return true;
    }
    if (getClass() != obj.getClass()) {
      return false;
    }

    TestReferenceFoo foo = (TestReferenceFoo) obj;
    return foo.getA().equals(this.a);
  }

  @Override
  public int hashCode() {
    return 'x';
  }
  public String getA() {
    return a;
  }

  public void setA(String a) {
    this.a = a;
  }

  public String getB() {
    return b;
  }

  public void setB(String b) {
    this.b = b;
  }

  public String getC() {
    return c;
  }

  public void setC(String c) {
    this.c = c;
  }
}
